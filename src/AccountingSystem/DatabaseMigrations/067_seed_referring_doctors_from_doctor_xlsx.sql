UPDATE target
SET IsActive = 1
FROM dbo.ReferringDoctors target
INNER JOIN #ReferringDoctorSeed seed ON seed.DoctorName = target.DoctorName
WHERE target.IsActive = 0;
GO

DECLARE @NextDoctorSequence INT;

SELECT @NextDoctorSequence = ISNULL(MAX(TRY_CONVERT(INT, RIGHT(DoctorCode, 4))), 0)
FROM dbo.ReferringDoctors
WHERE DoctorCode LIKE N'DOC-[0-9][0-9][0-9][0-9]';

;WITH MissingDoctors AS
(
    SELECT
        seed.DoctorName,
        ROW_NUMBER() OVER (ORDER BY seed.SortOrder) AS RowNumber
    FROM #ReferringDoctorSeed seed
    WHERE NOT EXISTS
    (
        SELECT 1
        FROM dbo.ReferringDoctors existing
        WHERE existing.DoctorName = seed.DoctorName
    )
)
INSERT INTO dbo.ReferringDoctors (DoctorCode, DoctorName, IsActive)
SELECT
    CONCAT(N'DOC-', RIGHT(CONCAT(N'0000', @NextDoctorSequence + RowNumber), 4)),
    DoctorName,
    1
FROM MissingDoctors;
GO

